                    <h3>The origins</h3>
                        <p>The commercial public screening of ten of Lumière brothers' short films in Paris on 28 December 1895 can be regarded as
                        the breakthrough of projected cinematographic motion pictures. There had been earlier cinematographic results and
                        screenings but these lacked either the quality or the momentum that propelled the cinématographe Lumière into a
                        worldwide success.
                        Soon film production companies were established all over the world. The first decade of motion picture saw film moving
                        from a novelty to an established mass entertainment industry.
