                    <h3>The origins</h3>
                        <p>The origins of theatre in ancient Greece, according to Aristotle, the first theoretician of theatre, are to be found in
                        the festivals that honoured Dionysus. The performances were given in semi-circular auditoria cut into hillsides, capable
                        of seating 10,000–20,000 people. The stage consisted of a dancing floor (orchestra), dressing room and scene-building
